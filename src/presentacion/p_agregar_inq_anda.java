    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        d_alquileres_anda inq = new d_alquileres_anda();
        d_inquilino i = new d_inquilino();
        p_control con = p_control.getInstancia();
        
        try {
            if (txtprop_id.getText().equals("")
                    || txtinq_casa.getText().equals("")
                    || txtidanda.getText().equals("")) {
                throw new Exception("FORMULARIO INCOMPLETO");
            }
            inq.setProp_id(Integer.parseInt(txtprop_id.getText()));
            inq.setInq_casa(Integer.parseInt(txtinq_casa.getText()));
            
            i = i.buscarinquilino(inq.getProp_id(), inq.getInq_casa());
            if (i == null) {
                throw new Exception("no se encuentra inquilino: " + inq.getProp_id() + "/" + inq.getInq_casa());
            }
            int ax = JOptionPane.showConfirmDialog(null, toUpperCase("Â¿desea agregar el ID ANDA " + txtidanda.getText() + " al inquilino: " + inq.getProp_id() + "/" + inq.getInq_casa() + "\n" + i.getInq_nombre()), "CONFIRMACION", JOptionPane.YES_NO_CANCEL_OPTION);
            if (ax == JOptionPane.YES_OPTION) {
                inq.setId_anda(txtidanda.getText());
                inq.guardar_alquiler_anda(inq);
                con.escribirfichero("se guarda id ANDA: " + inq.getId_anda() + " -- id prop.: " + inq.getProp_id() + " -- id inq.: " + inq.getInq_casa());
                JOptionPane.showMessageDialog(this, "SE INGRESO REGISTRO CORRECTAMENTE", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, toUpperCase(ex.getMessage()), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnagregarActionPerformed

