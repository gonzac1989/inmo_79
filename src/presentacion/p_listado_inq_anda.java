    private void tblinquilinosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblinquilinosMouseClicked
        int prop_id;
        int inq_casa;
        String nombre;
        try {
            int rows[] = tblinquilinos.getSelectedRows();
            int row = -1;

            for (int i = 0; i < rows.length; i++) {
                row = rows[i];
            }

            prop_id = Integer.parseInt(tblinquilinos.getValueAt(row, 0).toString());
            inq_casa = Integer.parseInt(tblinquilinos.getValueAt(row, 1).toString());
            nombre = ((String) tblinquilinos.getValueAt(row, 3));
            if (evt.getClickCount() == 2) {
                //doble clic
                int ax = JOptionPane.showConfirmDialog(null, toUpperCase("Â¿desea eliminar a: " + prop_id + " - " + inq_casa + "\n" + nombre + "?"), "CONFIRMACION", JOptionPane.YES_NO_CANCEL_OPTION);
                if (ax == JOptionPane.NO_OPTION) {
                    return;
                }
                if (ax == JOptionPane.CANCEL_OPTION) {
                    return;
                }
                if (ax == JOptionPane.YES_OPTION) {
                    d_alquileres_anda alq = new d_alquileres_anda();
                    alq.eliminar_alquiler_anda(prop_id, inq_casa);
                    JOptionPane.showMessageDialog(this, "REGISTRO ELIMINADO CORRECTAMENTE", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    cargar();
                }
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, toUpperCase(ex.getMessage()), "ERROR", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, toUpperCase(ex.getMessage()), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblinquilinosMouseClicked

