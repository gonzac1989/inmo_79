    void cargar() {
        d_alquileres_anda inq = new d_alquileres_anda();
        d_alquileres_anda inq1 = null;
        d_inquilino x = new d_inquilino();
        Vector v;
        String id_anda;

        try {
            cleartable();
            lista = inq.listar_alquileres_anda();
            for (d_alquileres_anda aux : lista) {
                inq1 = new d_alquileres_anda();
                v = new Vector();
                inq1.setProp_id(aux.getProp_id());
                inq1.setInq_casa(aux.getInq_casa());     
                id_anda=aux.getId_anda();
                inq1.setId_anda(x.buscarnombreinquilino(aux.getProp_id(), aux.getInq_casa()));                
                v.add(inq1.getProp_id());
                v.add(inq1.getInq_casa());
                v.add(id_anda);
                v.add(inq1.getId_anda());
                mdltabinq.addRow(v);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, toUpperCase(ex.getMessage()), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    void cleartable() {
        for (int i = 0; i < tblinquilinos.getRowCount(); i++) {
            mdltabinq.removeRow(i);
            i -= 1;
        }
    }

